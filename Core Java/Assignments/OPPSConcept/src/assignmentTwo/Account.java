package assignmentTwo;

public class Account {
	int accno;
	int amount;
	
	public Account(int accno2, int amount2) {
		this.accno = accno2;
		this.amount = amount2;
	}
	public int getAccno() {
		return accno;
	}
	public void setAccno(int accno) {
		this.accno = accno;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	
	
}
